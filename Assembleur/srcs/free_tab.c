#include "../includes/asm.h"

void		free_tab(char **tab)
{
	if (tab)
	{
		if (tab[0] && tab[1])
			free_tab(&(tab[1]));
		if (*tab)
			free(*tab);
		*tab = NULL;
		free(tab);
		tab = NULL;
	}
}
