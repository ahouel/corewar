#include "vm.h"

void	add_process(t_vm *vm, t_pcb *proc)
{
	t_pcb	*tmp;

	if (vm->proc)
	{
		proc->next = vm->proc;
		vm->proc = proc;
	}
	else
	{
		vm->proc = proc;
	}
}
